function replaceExactText(node, exactMap) {
  if (!node) return;
  const raw = (node.textContent || '').trim();
  if (!raw) return;
  if (Object.prototype.hasOwnProperty.call(exactMap, raw)) {
    node.textContent = exactMap[raw];
    return;
  }
  if (raw.includes(' · ')) {
    const parts = raw.split('·').map((part) => part.trim());
    const mapped = parts.map((part) => Object.prototype.hasOwnProperty.call(exactMap, part) ? exactMap[part] : part);
    const changed = mapped.some((part, index) => part !== parts[index]);
    if (changed) {
      node.textContent = mapped.join(' · ');
    }
  }
}

function replaceFragmentsInText(value, fragmentMap) {
  let next = String(value == null ? '' : value);
  Object.keys(fragmentMap).sort((a, b) => b.length - a.length).forEach((source) => {
    if (!source) return;
    next = next.split(source).join(fragmentMap[source]);
  });
  return next;
}

function translateTextNodes(exactMap, fragmentMap, lang) {
  if (!document.body || typeof document.createTreeWalker !== 'function') return;

  const walker = document.createTreeWalker(document.body, NodeFilter.SHOW_TEXT, {
    acceptNode(node) {
      if (!node || !node.nodeValue || !node.nodeValue.trim()) return NodeFilter.FILTER_REJECT;
      const parent = node.parentElement;
      if (!parent) return NodeFilter.FILTER_REJECT;
      const tag = String(parent.tagName || '').toUpperCase();
      if (tag === 'SCRIPT' || tag === 'STYLE' || tag === 'TEXTAREA') return NodeFilter.FILTER_REJECT;
      return NodeFilter.FILTER_ACCEPT;
    }
  });

  const nodes = [];
  while (walker.nextNode()) nodes.push(walker.currentNode);

  nodes.forEach((node) => {
    const rawValue = String(node.nodeValue || '');
    const trimmed = rawValue.trim();
    if (!trimmed) return;

    let nextValue = rawValue;

    if (Object.prototype.hasOwnProperty.call(exactMap, trimmed)) {
      nextValue = rawValue.replace(trimmed, exactMap[trimmed]);
    } else if (trimmed.includes(' · ')) {
      const parts = trimmed.split('·').map((part) => part.trim());
      const mapped = parts.map((part) => Object.prototype.hasOwnProperty.call(exactMap, part) ? exactMap[part] : part);
      const joined = mapped.join(' · ');
      if (joined !== trimmed) {
        nextValue = rawValue.replace(trimmed, joined);
      }
    }

    nextValue = replaceFragmentsInText(nextValue, fragmentMap);

    nextValue = nextValue.replace(/(\d+)\s+shown\s*\/\s*(\d+)\s+total\s*·\s*(\d+)\s+knowledge-ready/g, (_, shown, total, ready) => {
      return lang === 'ru'
        ? `${shown} показано / ${total} всего · ${ready} готово к знанию`
        : `${shown} shown / ${total} total · ${ready} knowledge-ready`;
    });

    nextValue = nextValue.replace(/^Updated:\s+/g, lang === 'ru' ? 'Обновлён: ' : 'Updated: ');
    nextValue = nextValue.replace(/^Updated\s+/g, lang === 'ru' ? 'Обновлён ' : 'Updated ');

    if (nextValue !== rawValue) {
      node.nodeValue = nextValue;
    }
  });
}

