function getLanguage(state) {
  return state && state.language === 'ru' ? 'ru' : 'en';
}

function getDict(state) {
  return DICTIONARY[getLanguage(state)] || DICTIONARY.en;
}

function setText(selector, value) {
  const node = document.querySelector(selector);
  if (node) node.textContent = value;
}

function setAllText(selector, value) {
  document.querySelectorAll(selector).forEach((node) => {
    node.textContent = value;
  });
}

function setAttribute(selector, name, value) {
  const node = document.querySelector(selector);
  if (node) node.setAttribute(name, value);
}

function withText(selector, callback) {
  document.querySelectorAll(selector).forEach((node, index) => callback(node, index));
}

function updateNavButtons(state) {
  const dict = getDict(state);
  document.querySelectorAll('#workspaceShellNav .workspace-nav-btn[data-section]').forEach((button) => {
    const key = button.getAttribute('data-section') || '';
    if (dict.nav[key]) button.textContent = dict.nav[key];
  });

  document.querySelectorAll('.cabinet-inner-nav-btn[data-section]').forEach((button) => {
    const key = button.getAttribute('data-section') || '';
    if (key === 'workspace') {
      button.textContent = dict.home;
      return;
    }
    if (dict.nav[key]) button.textContent = dict.nav[key];
  });
}

function updateFocusStrip(state) {
  const dict = getDict(state);
  const chips = document.querySelectorAll('#homeFocusStrip .workspace-home-focus-chip');
  if (chips[0]) chips[0].textContent = dict.activeFileNone;
  if (chips[1]) chips[1].textContent = dict.projectNone;
  if (chips[2]) chips[2].textContent = dict.noteNone;
}

function updateSourcePlaceholder(state, els) {
  if (!els || !els.addressInput) return;
  const dict = getDict(state);
  const sourceLabel = (els.sourceTriggerLabel && els.sourceTriggerLabel.textContent) || 'Google';
  els.addressInput.placeholder = dict.searchPlaceholder.replace('{source}', sourceLabel);
}

function updateLanguageButton(state) {
  const button = document.getElementById('languageToggleBtn');
  const dict = getDict(state);
  if (!button) return;
  button.textContent = getLanguage(state).toUpperCase();
  button.setAttribute('title', dict.languageTitle);
  button.setAttribute('aria-label', dict.languageTitle);
}


