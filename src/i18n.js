function startObserver() {
  if (observerStarted || typeof MutationObserver === 'undefined') return;
  observerStarted = true;

  const observer = new MutationObserver(() => {
    if (applying) return;
    if (observerTimeout) clearTimeout(observerTimeout);
    observerTimeout = setTimeout(() => {
      if (!lastState) return;
      applyLanguage(lastState, lastEls, { dispatchEvents: false });
    }, 60);
  });

  observer.observe(document.body, {
    childList: true,
    subtree: true,
    characterData: false
  });
}

export function ensureLanguageToggle(state, onToggle) {
  const host = document.querySelector('.topbar-actions');
  if (!host) return null;

  let button = document.getElementById('languageToggleBtn');
  if (!button) {
    button = document.createElement('button');
    button.type = 'button';
    button.id = 'languageToggleBtn';
    button.className = 'utility-btn';
    button.style.width = '52px';
    button.style.minWidth = '52px';
    button.style.fontSize = '12px';
    button.style.fontWeight = '700';
    button.style.letterSpacing = '0.08em';
    host.insertBefore(button, host.firstChild || null);
  }

  if (button.dataset.boundClick !== 'true') {
    button.dataset.boundClick = 'true';
    button.addEventListener('click', () => {
      const next = getLanguage(state) === 'en' ? 'ru' : 'en';
      if (typeof onToggle === 'function') onToggle(next);
    });
  }

  updateLanguageButton(state);
  startObserver();
  return button;
}

